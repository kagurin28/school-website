<div class="paragraph">
	<a id="Eshays"></a>
	<h2>Eshays</h2>
	<p>
		When you are at New Town High, you are going to see many Eshays. They often have mullets and are tough-looking. Eshays are known to wander around the school, even when they are ment to be in class. They occasionally hand out gum between themselves (gum is a banned item). As such, it is also common to find about 5 pieces of gum underneath any table or desk.
	</p>
	<p>
		Let me say though, with these guys around, you are in for a <i>wild</i> ride at New Town High. The sheer amount of things eshays have done just to make school life more interesting for everyone else is amazing. Truly an wonderful group of people.
	</p>
</div>

<div class="paragraph">
	<a id="Classes"></a>
	<h2>Classes</h2>
	<p>
		At New Town High School, there are many optional classes for you to choose between.
	</p>
	
	<div class="miniPara">
		<h3>Woodwork/Metalwork</h3>
		<p>
			These two subjects are some of the most popular subjects. As you would expect, you create things out of wood and metal. To me, it feels like working in a sweatshop, but some people really like it though. (Be aware that I do not have any experience in metalwork).
		</p>
	</div>
	<div class="miniPara">
		<h3>Japanese/French</h3>
		<p>
			The two language choices at New Town High are Japanese and French. Japanese is good if you're a weeb (obviously).  French is not really done by anyone tbh.<br>
			(Also, I think they offer trips to Tokoyo and Paris in Year 9/10, so that's pretty cool.)
		</p>
	</div>
	<div class="miniPara">
		<h3>Sports Fitness</h3>
		<p>
			I don't know much about this one, other than it is pretty much an extention to HPE (Health and Physical Education). It obviously attracts many eshays, some of the worst ones in the school, in fact.
		</p>
	</div>
	<div class="miniPara">
		<h3>Digital Technology</h3>
		<p>
			In Digi-Tech, you are supposed to do coding, although 90% of the class are not most of the time. This is a fairly popular class, but mostly because people see it as an opportunity to do random shit insted of actually doing work. This entire website was actually made for a Digi-Tech assignment, which is pretty neat.
		</p>
	</div>
	<div class="miniPara">
		<h3>Food Studies</h3>
		<p>
			Food Studies might be the most popular subject at New Town High School. This was really hard to get into, because there were so many people.<br>
			<br>
			Other than that, it's pretty self-explainatory; you just cook food.
		</p>
	</div>
	<div class="miniPara">
		<h3>Drama</h3>
		<p>
			I don't know much about this subject (and I've always hated acting), other than the teacher is basically a NTHS veteran.
		</p>
	</div>
